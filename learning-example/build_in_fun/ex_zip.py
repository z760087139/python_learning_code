# 将可迭代对象作参数，将对象中的元素打包成一个元组，返回元组组成的列表
# 若迭代器元素不一致，按照最短的对象进行
# python 2 返回列表  python 3 返回对象
a = [1,2,3,4,5,6]
b = [6,5,4,3,2,1]
c = [2,3,4,5,6,7]

d = zip(a,b,c)
print (d)
# >[(1, 6, 2), (2, 5, 3), (3, 4, 4), (4, 3, 5), (5, 2, 6), (6, 1, 7)]
# pypthon 3 : d = list(zip(a,b,c))
# 可以使用 * 进行解包

e = zip(*d)
print(e)
#[(1, 2, 3, 4, 5, 6), (6, 5, 4, 3, 2, 1), (2, 3, 4, 5, 6, 7)]